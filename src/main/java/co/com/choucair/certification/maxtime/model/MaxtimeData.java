package co.com.choucair.certification.maxtime.model;

public class MaxtimeData {

    private String username;
    private String password;
    private String proyecto;
    private String tipo_hora;
    private String servicio;
    private String actividad;
    private String horas_ejecutadas;
    private String comentario;


    public String getUsername() {return username;}
    public void setUsername(String username) {this.username = username;}
    public String getPassword() {return password;}
    public void setPassword(String password) {this.password = password;}
    public String getProyecto() {return proyecto;}
    public void setProyecto(String proyecto) {this.proyecto = proyecto;}
    public String getTipo_hora() {return tipo_hora;}
    public void setTipo_hora(String tipo_hora) {this.tipo_hora = tipo_hora;}
    public String getServicio() {return servicio;}
    public void setServicio(String servicio) {this.servicio = servicio;}
    public String getActividad() {return actividad;}
    public void setActividad(String actividad) {this.actividad = actividad;}
    public String getHoras_ejecutadas() {return horas_ejecutadas;}
    public void setHoras_ejecutadas(String horas_ejecutadas) {this.horas_ejecutadas = horas_ejecutadas;}
    public String getComentario() {return comentario;}
    public void setComentario(String comentario) {this.comentario = comentario;}
}
